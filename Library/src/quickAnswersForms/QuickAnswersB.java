     @Override
    public void showTable(ArrayList<Books> list) 
    {
        model.setRowCount(0);
        for (int i = 0 ; i < list.size() ; ++i)
        {
            Object[] row = {list.get(i).getISBN() , list.get(i).getTitle() , list.get(i).getCategory() , list.get(i).getPage()};
            model.addRow(row);
        }
    }
